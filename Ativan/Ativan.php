        <div class="alert alert-warning " role="alert">
            <p><b>Anxiety</b><br>
                Lorazepam has anxiety-reducing effects and its best-known indication is the short-term management of
                severe anxiety. In the US, the FDA advises against use of benzodiazepines such as lorazepam for longer
                than four weeks.It is fast acting, and useful in treating fast onset panic anxiety.<br>

                Lorazepam can effectively reduce agitation and induce sleep, and the duration of effects from a single
                dose makes it an appropriate choice for the short-term treatment of insomnia, especially in the presence
                of severe anxiety or night terrors. It has a fairly short duration of action.<br>

                Withdrawal symptoms, including rebound insomnia and rebound anxiety, may occur after seven days' use of
                lorazepam.<br>

                <b>Seizures</b><br>
                Intravenous diazepam or lorazepam are first-line treatments for convulsive status epilepticus.Lorazepam
                is more effective than diazepam and intravenous phenytoin in the treatment of status epilepticus and has
                a lower risk of continuing seizure that might require additional medication.However, phenobarbital has a
                superior success rate compared to lorazepam and other drugs, at least in the elderly.<br>

                Lorazepam's anticonvulsant properties and pharmacokinetic profile, make intravenous use reliable for
                terminating acute seizures, but induces prolonged sedation. Oral benzodiazepines, including lorazepam
                are occasionally used as long-term prophylactic treatment of resistant absence seizures; because of
                gradual tolerance to their anti-seizure effects, benzodiazepines such as lorazepam are not considered
                first-line therapies.<br>

                Lorazepam's anticonvulsant and CNS depressant properties are useful for the treatment and prevention of
                alcohol withdrawal syndrome. In this setting, impaired liver function is not a hazard with lorazepam,
                since lorazepam does not require oxidation, hepatic or otherwise, for its metabolism.<br>

                <b>Sedation</b><br>
                Lorazepam is sometimes used for individuals receiving mechanical ventilation. However, in critically ill
                people, propofol has been found to be superior to lorazepam both in effectiveness and overall cost; as a
                result, the use of propofol for this indication is now encouraged, whereas the use of lorazepam is
                discouraged.<br>

                Its relative effectiveness in preventing new memory formation, along with its ability to reduce
                agitation and anxiety, makes lorazepam useful as premedication. It is given before a general anesthetic
                to reduce the amount of anesthetic required, or before unpleasant awake procedures, such as in dentistry
                or endoscopies, to reduce anxiety, to increase compliance, and to induce amnesia for the procedure.
                Lorazepam by mouth is given 90 to 120 minutes before procedures, and intravenous lorazepam as late as 10
                minutes before procedures.Lorazepam is sometimes used as an alternative to midazolam in palliative
                sedation.In intensive care units lorazepam is sometimes used to produce anxiolysis, hypnosis, and
                amnesia.<br>

                <b>Agitation</b><br>
                Lorazepam is sometimes used as an alternative to haloperidol when there is the need for rapid sedation
                of violent or agitated individuals, but haloperidol plus promethazine is preferred due to better
                effectiveness and due to lorazepam's adverse effects on respiratory function.However, adverse effects
                such as behavioral disinhibition may make benzodiazepines inappropriate for some acutely psychotic
                patients.Acute delirium is sometimes treated with lorazepam, but as it can cause paradoxical effects, it
                is preferably given together with haloperidol.Lorazepam is absorbed relatively slowly if given
                intramuscularly, a common route in restraint situations.<br>

                <b>Other</b><br>
                Catatonia with inability to speak is responsive to lorazepam. Symptoms may recur and treatment for some
                days may be necessary. Catatonia due to abrupt or overly rapid withdrawal from benzodiazepines, as part
                of the benzodiazepine withdrawal syndrome, should also respond to lorazepam treatment.As lorazepam can
                have paradoxical effects, haloperidol is sometimes given at the same time.<br>

                It is sometimes used in chemotherapy in addition to medications used to treat nausea and vomiting, i.e.
                nausea and vomiting caused or worsened by psychological sensitization to the thought of being sick.It is
                also used as adjunct therapy for cyclic vomiting syndrome.

