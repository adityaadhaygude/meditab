        <div class="alert alert-warning " role="alert">
            <p>
                <b>Cough suppression<br></b>
                The primary use of dextromethorphan is as a cough suppressant, for the temporary relief of cough caused
                by minor throat and bronchial irritation (such as commonly accompanies the flu and common cold), as well
                as those resulting from inhaled particle irritants. However, controlled studies have found the
                symptomatic effectiveness of dextromethorphan similar to placebo.<br><br>

                <b>Neuropsychiatric disorders<br></b>
                In 2010, the FDA approved the combination drug dextromethorphan/quinidine for the treatment of
                pseudobulbar affect (uncontrollable laughing/crying). Dextromethorphan is the actual therapeutic agent
                in the combination; quinidine merely serves to inhibit the enzymatic degradation of dextromethorphan and
                thereby increase its circulating concentrations via inhibition of CYP2D6.In 2016, the ASA released a
                promising study with the combination of dextromethorphan with pregabalin, acetaminophen, and naproxen
                which showed a decrease in postoperative pain intensity (preemptive analgesia).<br><br>

                <b>Contraindications<br></b>
                Because dextromethorphan can trigger a histamine release (allergic reaction), atopic children, who are
                especially susceptible to allergic reactions, should be administered dextromethorphan only if absolutely
                necessary, and only under the strict supervision of a healthcare professional.<br>

