        <div class="alert alert-warning " role="alert">
            <p>
                <b>Fever</b><br>
                Paracetamol is used for reducing fever in people of all ages.The World Health Organization recommends
                that paracetamol be used to treat fever in children only if their temperature is higher than 38.5 °C
                (101.3 °F).The efficacy of paracetamol by itself in children with fevers has been questioned and a
                meta-analysis showed that it is less effective than ibuprofen.Paracetamol does not have significant
                anti-inflammatory effects.</p>
            <p>
                <b>Pain</b><br>
                Paracetamol is used for the relief of mild to moderate pain.The use of the intravenous form for
                short-term pain in people in the emergency department is supported by limited evidence.</p>
            <p>
                <b>Osteoarthritis</b><br>
                The American College of Rheumatology recommends paracetamol as one of several treatment options for
                people with arthritis pain of the hip, hand, or knee that does not improve with exercise and weight
                loss.A 2015 review, however, found it provided only a small benefit in osteoarthritis.<br>

                Paracetamol has relatively little anti-inflammatory activity, unlike other common analgesics such as the
                nonsteroidal anti-inflammatory drugs (NSAIDs) aspirin, and ibuprofen, but ibuprofen and paracetamol have
                similar effects in the treatment of headache. Paracetamol can relieve pain in mild arthritis, but has no
                effect on the underlying inflammation, redness, and swelling of the joint.It has analgesic properties
                comparable to those of aspirin, while its anti-inflammatory effects are weaker.It is better tolerated
                than aspirin due to concerns about bleeding with aspirin.</p>
            <p>
                <b>Lower back</b><br>
                Based on a systematic review, paracetamol is recommended by the American Pain Society as a first-line
                treatment for lower back pain.In contrast, the American College of Physicians found good evidence for
                NSAIDs but only fair evidence for paracetamol, while other systematic reviews have concluded that
                evidence for its efficacy is lacking entirely.</p>
            <p>
                <b>Headaches</b><br>
                A joint statement of the German, Austrian, and Swiss headache societies and the German Society of
                Neurology recommends the use of paracetamol in combination with caffeine as one of several first-line
                therapies for treatment of tension or migraine headache.In the treatment of acute migraine, it is
                superior to placebo, with 39% of people experiencing pain relief at 1 hour compared with 20% in the
                control group.</p>
            <p>
                <b>Postoperative</b><br>
                Paracetamol combined with NSAIDs may be more effective for treating postoperative pain than either
                paracetamol or NSAIDs alone.</p>
            <p>
                <b>Teeth</b><br>
                NSAIDs such as ibuprofen, naproxen, and diclofenac are more effective than paracetamol for controlling
                dental pain or pain arising from dental procedures; combinations of NSAIDs and paracetamol are more
                effective than either alone.Paracetamol is particularly useful when NSAIDs are contraindicated due to
                hypersensitivity or history of gastrointestinal ulceration or bleeding.It can also be used in
                combination with NSAIDs when these are ineffective in controlling dental pain alone.The Cochrane review
                of preoperative analgesics for additional pain relief in children and adolescents shows no evidence of
                benefit in taking paracetamol before dental treatment to help reduce pain after treatment for procedures
                under local anaesthetic, but the quality of evidence is low.</p>
            <p>
                <b>Other</b><br>
                The efficacy of paracetamol when used in combination with weak opioids (such as codeine) improved for
                about 50% of people, but with increases in the number experiencing side effects.Combination drugs of
                paracetamol and strong opioids such as morphine improve analgesic effect.<br>

                The combination of paracetamol with caffeine is superior to paracetamol alone for the treatment of
                common pain conditions, including dental pain, post partum pain, and headache.</p>
            <p>
                <b>Patent ductus arteriosus</b><br>
                Paracetamol is used to treat patent ductus arteriosus, a condition that affects newborns when a blood
                vessel used in developing the lungs fails to close as it normally does, but evidence for the safety and
                efficacy of paracetamol for this purpose is lacking.NSAIDs, particularly indomethacin and ibuprofen,
                have also been used, but the evidence for them is also not strong.The condition appears to be caused in
                part by overactive prostaglandin E2 (PGE2), signalling primarily through its EP4 receptor, but possibly
                also through its EP2 receptor and EP3 receptors.</p>
