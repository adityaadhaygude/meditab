            <p>Amlodipine is used in the management of hypertension and coronary artery disease in people with either
                stable angina (where chest pain occurs mostly after physical or emotional stress) or vasospastic angina
                (where it occurs in cycles) and without heart failure. It can be used as either monotherapy or
                combination therapy for the management of hypertension or coronary artery disease. Amlodipine can be
                administered to adults and children 6–17 years of age.<br>
                <br>
                <b>Combination therapy</b><br>
                Amlodipine can be given as a combination therapy with a variety of medications:<br>

                Amlodipine/atorvastatin, where amlodipine is given for hypertension or CAD and atorvastatin prevents
                cardiovascular events, or if the person also has high cholesterol.<br>
                Amlodipine/aliskiren or amlodipine/aliskiren/hydrochlorothiazide if amlodipine alone cannot reduce blood
                pressure. Aliskiren is a renin inhibitor, which works to reduce primary hypertension (that with no known
                cause) by binding to renin and preventing it from initiating the renin–angiotensin system (RAAS) pathway
                to increase blood pressure. Hydrochlorothiazide is a diuretic and decreases overall blood volume.<br>
                Amlodipine/benazepril if either drug has failed individually, or amlodipine alone caused edema.
                Benazepril is an ACE inhibitor and blocks the conversion of angiotensin I to angiotensin II in the RAAS
                pathway.<br>
                Amlodipine/olmesartan or amlodipine/olmesartan/hydrochlorothiazide if amlodipine is insufficient in
                reducing blood pressure. Olmesartan is an angiotensin II receptor antagonist and blocks part of the RAAS
                pathway.<br>
                Amlodipine/perindopril if using amlodipine alone caused edema. Perindopril is a long-lasting ACE
                inhibitor.
                Amlodipine/telmisartan, where telmisartan is an angiotensin II receptor antagonist.<br>
                Amlodipine/valsartan or amlodipine/valsartan/hydrochlorothiazide, where valsartan is an angiotensin II
                receptor antagonist.

