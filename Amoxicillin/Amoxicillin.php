        <div class="alert alert-warning " role="alert">
            <p>Amoxicillin is used in the treatment of a number of infections, including acute otitis media,
                streptococcal pharyngitis, pneumonia, skin infections, urinary tract infections, Salmonella infections,
                Lyme disease, and chlamydia infections.<br>

                <b>Acute otitis media:</b><br>
                Children with acute otitis media who are younger than 6 months of age are generally treated with
                amoxicillin or other antibiotic. Although most children with acute otitis media who are older than two
                years old do not benefit from treatment with amoxicillin or other antibiotic, such treatment may be
                helpful in children younger than two years old with acute otitis media that is bilateral or accompanied
                by ear drainage.In the past, amoxicillin was dosed three times daily when used to treat acute otitis
                media, which resulted in missed doses in routine ambulatory practice. There is now evidence that two
                times daily dosing or once daily dosing has similar effectiveness.<br>

                <b>Respiratory infections:</b><br>
                Amoxicillin and amoxicillin-clavulanate have been recommended by guidelines as the drug of choice for
                bacterial sinusitis and other respiratory infections.Most sinusitis infections are caused by viruses,
                for which amoxicillin and amoxicillin-clavulanate are ineffective,and the small benefit gained by
                amoxicillin may be overridden by the adverse effects.Amoxicillin is recommended as the preferred
                first-line treatment for community-acquired pneumonia in adults by the National Institute for Health and
                Care Excellence, either alone (mild to moderate severity disease) or in combination with a macrolide.The
                World Health Organization recommends amoxicillin as first-line treatment for pneumonia that is not
                "severe".Amoxicillin is used in post-exposure inhalation of anthrax to prevent disease progression and
                for prophylaxis.<br>

                <b>H. pylori:</b><br>
                It is effective as one part of a multi-drug regimen for treatment of stomach infections of Helicobacter
                pylori. It is typically combined with a proton-pump inhibitor (such as omeprazole) and a macrolide
                antibiotic (such as clarithromycin); other drug combinations are also effective.<br>

                <b>Lyme borreliosis:</b><br>
                Amoxicillin is effective for treatment of early cutaneous Lyme borreliosis; the effectiveness and safety
                of oral amoxicillin is neither better nor worse than common alternatively-used antibiotics.<br>

                <b>Skin infections:</b><br>
                Amoxicillin is occasionally used for the treatment of skin infections, such as acne vulgaris.It is often
                an effective treatment for cases of acne vulgaris that have responded poorly to other antibiotics, such
                as doxycycline and minocycline.<br>

                <b>Infections in infants in resource-limited settings:</b><br>
                Amoxicillin is recommended by the World Health Organization for the treatment of infants with signs and
                symptoms of pneumonia in resource-limited situations when the parents are unable or unwilling to accept
                hospitalization of the child. Amoxicillin in combination with gentamicin is recommended for the
                treatment of infants with signs of other severe infections when hospitalization is not an option.<br>

                <b>Prevention of bacterial endocarditis:</b><br>
                It is also used to prevent bacterial endocarditis in high-risk people having dental work done, to
                prevent Streptococcus pneumoniae and other encapsulated bacterial infections in those without spleens,
                such as people with sickle-cell disease, and for both the prevention and the treatment of anthrax.The
                United Kingdom recommends against its use for infectious endocarditis prophylaxis.These recommendations
                do not appear to have changed the rates of infection for infectious endocarditis.

